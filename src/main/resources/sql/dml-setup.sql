-- =====================================================================
-- 파트 B: 스키마 생성 (DDL - Tables and Primary Keys)
-- 8개의 테이블을 생성하고 기본 키, 고유 키, Not Null 제약조건을 정의합니다.
-- =====================================================================

-- 테이블 1: USERS (사용자)
CREATE TABLE USERS (
    user_id           BIGINT          PRIMARY KEY,
    username          VARCHAR(50)    UNIQUE NOT NULL,
    email             VARCHAR(100)   UNIQUE NOT NULL,
    registration_date DATE            NOT NULL,
    last_login_date   DATE,
    manager_id        BIGINT          -- 자기참조 외래 키
);
ALTER TABLE USERS COMMENT = '애플리케이션 사용자 정보를 저장하는 테이블';
-- COMMENT ON COLUMN USERS.user_id IS '사용자 고유 ID (PK)';
-- COMMENT ON COLUMN USERS.manager_id IS '관리자 ID (자기참조용)';


-- 테이블 2: USER_PROFILES (사용자 프로필) - USERS와 1:1 관계
CREATE TABLE USER_PROFILES (
    user_id             BIGINT          PRIMARY KEY,
    full_name           VARCHAR(100),
    bio                 VARCHAR(255),
    profile_picture_url VARCHAR(255)
);
ALTER TABLE USER_PROFILES COMMENT = '사용자의 부가적인 프로필 정보를 저장하는 테이블 (USERS와 1:1)';


-- 테이블 3: POSTS (게시물)
CREATE TABLE POSTS (
    post_id       BIGINT          PRIMARY KEY,
    user_id       BIGINT          NOT NULL,
    content       VARCHAR(1000),
    post_type     VARCHAR(10)    CHECK (post_type IN ('photo', 'video')),
    creation_date DATETIME       NOT NULL
);
ALTER TABLE POSTS COMMENT = '사용자가 작성한 게시물 정보를 저장하는 테이블';


-- 테이블 4: COMMENTS (댓글)
CREATE TABLE COMMENTS (
    comment_id        BIGINT          PRIMARY KEY,
    post_id           BIGINT          NOT NULL,
    user_id           BIGINT          NOT NULL,
    comment_text      VARCHAR(1000)  NOT NULL,
    creation_date     DATE            NOT NULL,
    parent_comment_id BIGINT          -- 계층 구조를 위한 자기참조 외래 키
);
ALTER TABLE COMMENTS COMMENT = '게시물에 달린 댓글 정보를 저장하는 테이블';
-- COMMENT ON COLUMN COMMENTS.parent_comment_id IS '부모 댓글 ID (계층형 쿼리용)';


-- 테이블 5: HASHTAGS (해시태그)
CREATE TABLE HASHTAGS (
    tag_id    BIGINT          PRIMARY KEY,
    tag_name  VARCHAR(100)   UNIQUE NOT NULL
);
ALTER TABLE HASHTAGS COMMENT = '고유한 해시태그 목록을 관리하는 조회용 테이블';


-- 테이블 6: LIKES (좋아요) - USERS와 POSTS의 N:M 관계 해소
CREATE TABLE LIKES (
    user_id       BIGINT,
    post_id       BIGINT,
    creation_date DATE    NOT NULL,
    PRIMARY KEY (user_id, post_id)
);
ALTER TABLE LIKES COMMENT = '사용자와 게시물 간의 "좋아요" 관계를 나타내는 연결 테이블';


-- 테이블 7: FOLLOWS (팔로우) - USERS와 USERS의 N:M 관계 해소
CREATE TABLE FOLLOWS (
    follower_id   BIGINT,
    following_id  BIGINT,
    creation_date DATE    NOT NULL,
    PRIMARY KEY (follower_id, following_id)
);
ALTER TABLE FOLLOWS COMMENT = '사용자 간의 "팔로우" 관계를 나타내는 연결 테이블';


-- 테이블 8: POST_TAGS (게시물-태그) - POSTS와 HASHTAGS의 N:M 관계 해소
CREATE TABLE POST_TAGS (
    post_id BIGINT,
    tag_id  BIGINT,
    PRIMARY KEY (post_id, tag_id)
);
ALTER TABLE POST_TAGS COMMENT = '게시물과 해시태그의 관계를 나타내는 교차 테이블';



